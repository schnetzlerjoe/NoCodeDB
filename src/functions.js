import { sum, add, subtract, divide, multiply } from 'mathjs'

// Sum function
export const sum = (...args) => {
  return sum(args)
}

// Add function
export const add = (a, b) => {
  return add(a, b)
}

// Subtract function
export const subtract = (a, b) => {
  return subtract(a, b)
}

// Divide function
export const divide = (a, b) => {
  return divide(a, b)
}

// Subtract function
export const multiply = (a, b) => {
  return multiply(a, b)
}